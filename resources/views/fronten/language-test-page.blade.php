                                            <p class="text-justify">The origin of the Japanese language involves a history of migration and cultural blending. Throughout its history, Japanese absorbed influences from various languages. The Ainu people from the Hokkaido islands of Japan, contributed a few place names besides the Chinese and introduced significant vocabulary. Whereas, Portuguese added terms during the 16th century, and Dutch brought scientific words in the 1600s. During the <strong>post-World War-II*</strong> many English words were also introduced.</p>
                                            
