                                            <p>The three scripts of Japanese Language are Hiragana, Katakana and Kanji.</p>
                                            <ol>
                                                <li class="text-justify"><strong>Hiragana:</strong> Hiragana is one of the first scripts in the <strong>Japanese language</strong> that a learner has to learn at the beginning. Hiragana consists of 46 characters. This script is widely used in materials like comics, textbooks, and animations.</li>
                                                <li class="text-justify"><strong>Katakana:</strong> Katakana is a Japanese writing style, like Hiragana and Kanji. The Katakana script is also known as ‘fragmentary kana’ because its characters have originated from parts that are more complex than Kanji. </li>
                                                <li class="text-justify"><strong>Kanji:</strong> Kanji is also one of the oldest scripts in Japan, that is derived from Chinese characters introduced over 1500 years ago. The Kanji script includes 50,000 characters and also uses small symbols in 13 different positions to create complete characters. </li>
                                            </ol>
