                                            <h2 style="font-size:20px;color:#4197a2;padding-bottom:10px" class="mt-5">Dialects of the Japanese Language</h2>
                                            <p class="text-justify">Japan has 8 (eight) regions in total, they are <strong> Hokkaido, Tohoku, Kanto, Chubu, Kansai, Chugoku, Shikoku,</strong> and <strong> Kyushu,</strong> where people speak in different dialects.</p>
                                            <p>Some of those major dialects can be understood as</p>
                                            <ul>
                                                <li class="text-justify"><strong>Kanto-ben:</strong> This is a standard dialect of the Japanese language, spoken in Tokyo. Kanto-ben features interjections, ending like “-be”, or “-ppe”.</li>
                                                <li class="text-justify"><strong> Kansai-ben:</strong> This dialect is spoken in Osaka, Kyoto and its adjoining regions.This dialect has fast speech like “nai” that changes to “hen”, “ne” to “na”, “da” to “de”, “ii” to “ee”.</li>
                                                <li class="text-justify"><strong> Tohoku-ben:</strong> The Tohoku-ben dialect is spoken in the Tohoku Region, which features a slow and nasalized speech with the use of “nga” for “ga”, and “iku” to “igu”.</li>
                                                <li class="text-justify"><strong>Kyushu-ben:</strong> This is a dialect of Japanese spoken in the Kyushu region which features “i” at the end of adjectives. </li>
                                                <li class="text-justify"><strong>Hokkaido-ben:</strong> The Hokkaido dialect is influenced by the Tohoku Dialect. This dialect features a closeness to standard Japanese, and includes the use of unique expressions.</li>
                                                <li class="text-justify"><strong>Chubu-dialect:</strong> This dialect is mostly spoken in Central Japan.</li>
                                                <li class="text-justify"><strong>Okinawa dialect:</strong> The Okinawa dialect is spoken in the Okinawa Island region of Japan. This dialect is distinct from the mainland Japanese dialects that are usually spoken, and includes expressions like “mensore” (welcome) and “haisai” (hello).</li>
