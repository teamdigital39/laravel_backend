                                            <p style="font-size:20px;color:#4197a2;padding-bottom:10px" class="mt-5">Japanese Grammar</p>
                                            <ul>
                                                <li class="text-justify"><strong>Flexible Sentence Structuring:</strong> Japanese sentences are flexibly structured like English Sentences as they can be omitted with placing the subject and objects in a variable order. </li>
                                                <li class="text-justify"><strong>Simple Tense System:</strong> Japanese grammar features only present and past tenses, unlike English which has a distinct future tense. In Japanese, future events are expressed using the present tense along with specific time indicators to convey the future context. </li>
                                                <li class="text-justify"><strong>No Singular and Plural:</strong> The Japanese grammar does not differentiate between singular and plural forms.</li>
                                                <li class="text-justify"><strong>Non-use of Articles:</strong> In the <strong>Japanese language</strong> no articles are used for specifying genders, or numbers, unlike languages such as French and Spanish, which have gendered articles for nouns.</li>
                                            </ul>
